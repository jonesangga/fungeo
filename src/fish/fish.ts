function create(coords: number[][]): Picture {
    let unit = new Picture(16, 16);
    let t = coords.map(c => {
        return {
            x1: c[0],
            y1: c[1],
            x2: c[2],
            y2: c[3],
        }
    });
    return unit.segments(t);
}

